1)   Informações Pessoais – Quais dados utilizamos

 

A BR UNIQUE SEGUROS E BENEFICIOS respeita e investe no respeito da sua privacidade. Qualquer informação que você nos forneça será tratada com o mais alto nível de cuidado e segurança, sendo utilizada apenas de acordo com os limites estabelecidos na lei e neste documento.

 

“Informações Pessoais” são informações que permitem que você seja identificado direta ou indiretamente. Informações pessoais pode incluir seu nome, endereço, telefone, celular (whatsapp),  e-mail, endereço IP, histórico de crédito, número da carteira de identidade, número do cadastro de pessoa física, número do cartão de crédito ou débito, informações sobre sinistros ou apólices, informações sobre sua saúde, saldos financeiros, declaração de imposto de renda e outras informações fiscais, assim como outros dados sobre você ou qualquer projeto do qual participe ou pretenda participar, cujas informações não sejam de conhecimento público e não estejam disponíveis em bancos de dados públicos.

 

Caso nos envie Informações Pessoais referentes a outras entidades, pessoas jurídicas ou pessoas físicas, você declara ter a autoridade para fazê-lo e declara ter obtido o consentimento necessário ou ter a legitimidade necessária para autorizar o uso de tais informações nos termos desta Política.

 

No processo de “contato” do site, para realizar a comunicação com a BR UNIQUE SEGUROS E BENEFICIOS, podemos coletar o seu nome, telefone, e-mail, celular (whatsapp), CPF, CNPJ, por exemplo. Essas informações são necessárias para identificarmos você e viabilizarmos o contato esperado.

 

A BR UNIQUE SEGUROS E BENEFICIOS poderá coletar as informações inseridas ativamente por você, Usuário, no momento do contato, e informações coletadas automaticamente , ao utilizar os nossos serviços, como, por exemplo, dados atrelados a sua conexão, tais como: endereço de IP, data, hora, dispositivo utilizado, dados de consumo e hábitos de utilização, dentre outras informações.

 

A BR UNIQUE SEGUROS E BENEFICIOS poderá, adicionalmente e mediante solicitação e comunicação com você, por formas diversas, obter informações adicionais àquelas obtidas através do nosso site, necessárias para, direta ou indiretamente, realizar cotações, orçamentos, propostas de seguros ou emitir uma apólice de seguro ou um contrato de plano de saúde, de plano de previdência, de plano de consórcio ou ainda um contrato de financiamento conosco.

 

Poderá haver, assim, o tratamento de dois tipos de dados pessoais: (i) aqueles fornecidos voluntariamente pelo próprio usuário; e (ii) aqueles coletados automaticamente pela BR UNIQUE SEGUROS E BENEFICIOS.

 

(i) Dados pessoais fornecidos pelo usuário: A BR UNIQUE SEGUROS E BENEFICIOS coleta todos os dados pessoais inseridos ou encaminhados ativamente pelo usuário ao preencher os formulários de contato no site ou nas suas landing pages, inclusive através de chatbots, whatsapp, e-mails, etc. São eles: Nome, sobrenome, endereço de e-mail, telefone, celular (whatsapp), CPF/CNPJ, documento de identidade, estado civil, data de nascimento, gênero, sites e perfis em redes sociais, etc. Dados adicionais poderão ser eventualmente solicitados ou fornecidos quando da utilização do site ou das landing pages.

 

Independentemente de quais dados pessoais são fornecidos ativamente pelo usuário, nós apenas faremos uso daqueles efetivamente relevantes e necessários para o alcance das finalidades declaradas; e

 

(ii) Dados coletados automaticamente: A BR UNIQUE SEGUROS E BENEFICIOS também coleta uma série de informações de modo automático, tais como: características do dispositivo de acesso, do navegador, IP (com data e hora), origem do IP, informações sobre cliques, páginas acessadas, termos de procura digitado em nossos ambientes, dentre outros. Para tal coleta, faz-se o uso de algumas tecnologias padrões, como ferramentas de análise da jornada e ações do consumidor dentro do site e landing pages da BR UNIQUE SEGUROS E BENEFICIOS, cookies, pixel tags, beacons e local shared objects, que são utilizadas com o propósito de melhorar a sua experiência de navegação em nossos serviços, de acordo com os seus hábitos e as suas preferências.

 

A BR UNIQUE SEGUROS E BENEFICIOS, para as finalidades gerais aqui dispostas, não lida normalmente com dados pessoais definidos como sensíveis pela LGPD, entendidos como aqueles relacionados: à origem racial ou étnica, convicção religiosa, opinião política, filiação a sindicato ou a organização de caráter religioso, filosófico ou político, dado referente à saúde ou à vida sexual, dado genético ou biométrico. Dessa forma não temos a intenção de coletar ou processar dados pessoais sensíveis no curso normal de suas interações com nossos produtos ou serviços, salvo para atender suas solicitações ligadas aos seguros saúde, odontológico e/ou vida pretendido ou contratado, bem como relacionadas às suas solicitações referentes aos planos de saúde e/ou odontológico pretendido ou contratado, caso em que serão coletados apenas os seus dados referentes à sua saúde. Quando houver legitimo interesse ou for necessário processar seus dados pessoais sensíveis, por qualquer motivo, nós obteremos seu prévio, expresso e formal consentimento para qualquer processamento que for voluntário (por exemplo, para finalidades de marketing). Se processarmos seus dados pessoais sensíveis para outras finalidades, Nós nos apoiamos nas seguintes bases legais: (i) detecção e prevenção de crime; e (ii) cumprimento da lei aplicável.

 

Sobre de Dados Pessoais de Crianças e Adolescentes

 

No caso de ocorrer o tratamento de dados pessoais de indivíduos de até 12 anos, solicitaremos o consentimento específico e destacado do usuário ou dos pais e responsáveis deste, e/ou realizaremos o tratamento destas informações com base nas demais hipóteses autorizadoras trazidas pela Lei Geral de Proteção de Dados Pessoais – “LGPD”.

 

