·            Quando fornecidas por você: Informações como sua localização, assim como outras informações, como meios de comunicação preferidos, são coletadas quando você fornece essas informações voluntariamente. Exceto quando combinadas com as Informações Pessoais, essas informações não revelam a sua identidade ou a identidade de qualquer outro usuário dos Serviços.

·            Uso de cookies: Informações sobre o seu uso dos Serviços podem ser coletadas por terceiros, por meio de cookies. Cookies são informações armazenadas diretamente no computador que você está utilizando. Os cookies, as preferências de idioma, e outros dados de tráfego anônimos. Nos permitem a coleta de informações tais como o tipo de navegador, o tempo dispendido nos Serviços, as páginas visitadas e nossos prestadores de serviço utilizamos informações para proteção de segurança, para facilitar a navegação, exibir informações de modo mais eficiente, e personalizar sua experiência  ao utilizar nossos Serviços, assim como para rastreamento online. Também coletamos informações estatísticas sobre o uso dos nossos Serviços para aprimoramento contínuo do nosso design e funcionalidade, para entender como os Serviços são utilizados e para auxiliá-lo a solucionar questões relativas aos Serviços.

 

Sobre o uso de Cookies, arquivos de registros (logs) e similares

 

Cookies são pequenos arquivos de texto que podem ser colocados no seu computador ou dispositivo portátil por sites ou serviços na web que Você utiliza. São utilizados para garantir o bom funcionamento de sites e demais serviços online, assim como para fornecer informações aos proprietários do site ou serviço online.

 

Cookies são utilizados para melhorar o uso e a funcionalidade dos sites e demais serviços na web compatíveis da BR UNIQUE SEGUROS E BENEFICIOS e entender melhor como nossos visitantes utilizam os nosso site e serviços online, bem como as ferramentas e serviços por eles oferecidos. Os Cookies nos facilitam o processo de adaptar o site da BR UNIQUE SEGUROS E BENEFICIOS às suas necessidades pessoais, facilitando sempre que possível o seu uso, receber feedback da satisfação dos nossos consumidores.

 

Em geral, os cookies utilizados no WebSite podem ser divididos de acordo com as seguintes categorias:

 

·       Cookies de sessão: São Cookies de uso temporário, que são excluídos no momento em que Você fecha o seu navegador. Quando Você reinicia o seu navegador e volta para o site que criou o cookie, esse site trata Você como um novo visitante.

 

·       Cookies persistentes: São aqueles Cookies que permanecem no seu navegador até Você deletá-los manualmente ou até o seu navegador deletá-los de acordo com o período de duração estabelecido pelo cookie. Esses Cookies reconhecerão seu retorno como visitante a um site ou serviço da BR UNIQUE SEGUROS E BENEFICIOS.

 

·       Cookies necessários: São Cookies estritamente necessários para a operação de um site ou serviço compatível da BR UNIQUE SEGUROS E BENEFICIOS. Eles permitem que você navegue pelo site e use nossos recursos.

 

·       Cookies analíticos: São os cookies nos ajudam a entender como os visitantes  interagem  com as  páginas da BR UNIQUE SEGUROS E BENEFICIOS, fornecendo   informações   sobre  as áreas visitadas, o tempo de visita ao site e quaisquer problemas encontrados, como mensagens de erro.

 

·       Cookies funcionais e preferenciais: Os cookies permitem que as páginas da BR UNIQUE SEGUROS E BENEFICIOS se lembrem de suas escolhas, para proporcionar uma experiência personalizada. Também, possibilitam que os Usuários assistam a vídeos e utilizem ferramentas sociais, campos para comentários, dentre outros.

 

·       Cookies de Marketing:  Os cookies são utilizados para fornecer mais conteúdo relevante e do interesse dos Usuários. Podem ser utilizados para apresentar publicidade com um maior direcionamento ou limitar o número a que esta é veiculada, nas páginas da BR UNIQUE SEGUROS E BENEFICIOS. Também, permitem a medição da eficácia de uma campanha publicitária lançada.

 

·       Cookies que nos mandam informações sobre Você: Nós colocamos esse tipo de Cookies em um site ou serviço compatível da BR UNIQUE SEGUROS E BENEFICIOS e este tipo de Cookie só pode ser lido por nosso site e serviços compatíveis.

 

·       Cookies de redes sociais: Estes cookies são utilizados quando você partilha informação nas redes sociais, ou de alguma forma tem acesso aos nossos conteúdos através das redes sociais como o Facebook, Instragam ou Google+.

 

Os sites e serviços compatíveis da BR UNIQUE SEGUROS E BENEFICIOS também podem usar outras tecnologias de rastreamento similares a Cookies, que podem coletar informações tais como endereços IP, arquivos de registro e sinalizadores da web, dentre outras. Esses dados também são utilizados para nos ajudar a adaptar os sites e demais serviços compatíveis da BR UNIQUE SEGUROS E BENEFICIOS às suas necessidades pessoais.

 

